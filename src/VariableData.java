public class VariableData { // Define a public class named VariableData.
    public static void main(String[] args){ // The main method is the entry point of the program.
        int a = 10; // Declare an integer variable 'a' and initialize it to 10.
        int b = 20; // Declare an integer variable 'b' and initialize it to 20.
        int result = a + b; // Declare an integer variable 'result' and assign it the sum of 'a' and 'b'.
        System.out.println(result); // Print the value of 'result' to the console.

        char Data = 'A'; // Declare a character variable 'Data' and initialize it to 'A'.
        System.out.println(Data); // Print the value of 'Data' to the console.

        String Name = "Yes"; // Declare a string variable 'Name' and initialize it to "Yes".
        System.out.println(Name); // Print the value of 'Name' to the console.
    }
}
