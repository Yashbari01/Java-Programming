// This is a declaration of a public class named Main.
// A public class means it can be accessed by other classes and it matches the file name.
public class Main {

    // This is the main method, which serves as the entry point of the program.
    // The 'public' keyword means that this method is accessible from outside the class.
    // 'static' means it belongs to the class rather than an instance of the class.
    // 'void' means it does not return any value.
    // 'main' is the name of the method that is recognized by the JVM as the starting point.
    // 'String[] args' is an array of strings used for command-line arguments.
    public static void main(String[] args) {

        // This line prints the text "Hello World" to the console.
        // 'System.out' refers to the standard output stream.
        // 'println' is a method that prints the text followed by a newline.
        System.out.println("Hello World");
    }
}
